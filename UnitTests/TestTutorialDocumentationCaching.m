writeTextFile(fullfile(helperRoot, 'tutorialCacheHelperIncrement.m'), { ...
    'function tutorialCacheHelperIncrement(counterPath)', ...
    'counterPath = string(counterPath);', ...
    'count = 0;', ...
    'if isfile(counterPath)', ...
    '    storedCount = str2double(strtrim(fileread(counterPath)));', ...
    '    if ~isnan(storedCount)', ...
    '        count = storedCount;', ...
    '    end', ...
    'end', ...
    'fileID = fopen(counterPath, "w");', ...
    'assert(fileID ~= -1, "Could not open counter file for writing.");', ...
    'cleanup = onCleanup(@() fclose(fileID)); %#ok<NASGU>', ...
    'fprintf(fileID, "%d", count + 1);', ...
    'end'});

writeTextFile(fullfile(helperRoot, 'tutorialCacheHelperWriteMovie.m'), { ...
    'function tutorialCacheHelperWriteMovie(moviePath, posterPath, counterPath)', ...
    'moviePath = string(moviePath);', ...
    'posterPath = string(posterPath);', ...
    'tutorialCacheHelperIncrement(counterPath);', ...
    'fileID = fopen(moviePath, "w");', ...
    'assert(fileID ~= -1, "Could not open movie output for writing.");', ...
    'cleanupMovie = onCleanup(@() fclose(fileID)); %#ok<NASGU>', ...
    'fprintf(fileID, "demo movie");', ...
    'if posterPath ~= ""', ...
    '    imwrite(uint8(255), posterPath);', ...
    'end', ...
    'end'});

writeTutorialSource(tutorialPath, executionCountPath, movieBuildCountPath, "");

