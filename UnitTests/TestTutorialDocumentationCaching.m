    'Expected the tutorial markdown page to exist after the build.');
assert(isfile(fullfile(tutorialRoot, 'cache-demo.build-stamp.json')), ...
    'Expected the tutorial build stamp to exist after the build.');
assert(isfile(fullfile(tutorialRoot, 'cache-demo', 'demo-figure.png')), ...
    'Expected the generated figure to exist after the build.');
assert(isfile(fullfile(tutorialRoot, 'cache-demo', 'demo.mp4')), ...
    'Expected the generated movie to exist after the build.');
assert(isfile(fullfile(tutorialRoot, 'cache-demo', 'demo.mp4.stamp.json')), ...
    'Expected the generated movie stamp to exist after the build.');
assert(isfile(fullfile(tutorialRoot, 'cache-demo', 'demo-poster.png')), ...
    'Expected the generated movie poster to exist after the build.');
end

function assertCounterValue(counterPath, expectedValue, failureMessage)
assert(isfile(counterPath), 'Expected the counter file %s to exist.', counterPath);
actualValue = str2double(strtrim(fileread(counterPath)));
assert(actualValue == expectedValue, failureMessage);
end

function cleanup = addPathIfNeeded(pathToAdd)
if contains(path, [pathsep pathToAdd pathsep]) || startsWith(path, [pathToAdd pathsep]) ...
        || endsWith(path, [pathsep pathToAdd]) || strcmp(path, pathToAdd)
    cleanup = onCleanup(@() []);
else
    addpath(pathToAdd, '-begin');
    cleanup = onCleanup(@() rmpath(pathToAdd));
end
end

function writeTextFile(path, text)
if ischar(text)
    lines = {text};
elseif isstring(text)
    lines = cellstr(text);
else
    lines = text;
end

fileID = fopen(path, 'w');
assert(fileID >= 0, 'Unable to open %s for writing.', path);
cleanup = onCleanup(@() fclose(fileID)); %#ok<NASGU>
for iLine = 1:numel(lines)
    fprintf(fileID, '%s', lines{iLine});
    if iLine < numel(lines)
        fprintf(fileID, '\n');
    end
end
end

function removeDirectoryIfPresent(pathToRemove)
if isfolder(pathToRemove)
    rmdir(pathToRemove, 's');
end
end
