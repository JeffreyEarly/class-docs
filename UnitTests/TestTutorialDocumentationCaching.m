function buildTutorial(tutorialPath, buildFolder, previousBuildFolder, helperRoot, rebuildTutorials)
tutorialDocumentation = TutorialDocumentation.documentationFromSourceFiles( ...
    tutorialPath, ...
    buildFolder=buildFolder, ...
    previousBuildFolder=previousBuildFolder, ...
    sourceRoot=helperRoot, ...
    executionPaths=string(helperRoot), ...
    rebuildTutorials=rebuildTutorials);
tutorialDocumentation.writeToFile();
end

